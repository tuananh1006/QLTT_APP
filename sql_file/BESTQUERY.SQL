CREATE VIEW BXH1 AS
WITH TMP1 AS
(
    SELECT 
        teamID,
        SUM(CASE WHEN result = 'win' THEN 1 ELSE 0 END) AS wins,
        SUM(CASE WHEN result = 'lose' THEN 1 ELSE 0 END) AS losses
    FROM (
        SELECT
            teamoneID AS teamID,
            CASE 
                WHEN teamoneScore > teamtwoScore THEN 'win'
                ELSE 'lose'
            END AS result
        FROM MATCH
        UNION ALL
        SELECT
            teamtwoID AS teamID,
            CASE 
                WHEN teamtwoScore > teamoneScore THEN 'win'
                ELSE 'lose'
            END AS result
        FROM MATCH
    ) AS results
    GROUP BY teamID
)
SELECT 
    ROW_NUMBER() OVER (ORDER BY TES.TEAMTOTALSCORE DESC) AS RANKING,
    T.teamLogo,
    T.TEAMNAME,
    CONCAT(TMP1.wins, '-', TMP1.losses) AS [T/B],
    TES.TEAMTOTALSCORE,
	s.seasonName
FROM 
    TEAM_ESPORT_SEASON TES
    JOIN TEAM_ESPORT T ON TES.TEAMID = T.TEAMID
    JOIN SEASON S ON TES.SEASONID = S.SEASONID
    JOIN TMP1 ON TES.TEAMID = TMP1.teamID
GROUP BY 
    T.teamLogo,
    T.TEAMNAME,
    TMP1.wins,
    TMP1.losses,
    TES.TEAMTOTALSCORE,
	s.seasonName;

